    public function getAvatar($string, $widthHeight = 12, $theme = 'default')
    {
        $widthHeight = max($widthHeight, 12);
        $md5 = md5($string);

        $fileName = _TMP_DIR_ . '/' . $md5 . '.png';

        if ($this->tmpFileExists($fileName)) {
            return $fileName;
        }

        // Create seed.
        $seed = intval(substr($md5, 0, 6), 16);
        mt_srand($seed);

        $body = array(
            'legs' => mt_rand(0, count($this->availableParts[$theme]['legs']) - 1),
            'hair' => mt_rand(0, count($this->availableParts[$theme]['hair']) - 1),
            'arms' => mt_rand(0, count($this->availableParts[$theme]['arms']) - 1),
            'body' => mt_rand(0, count($this->availableParts[$theme]['body']) - 1),
            'eyes' => mt_rand(0, count($this->availableParts[$theme]['eyes']) - 1),
            'mouth' => mt_rand(0, count($this->availableParts[$theme]['mouth']) - 1)
        );

        // Avatar random parts.
        $parts = array(
            'legs' => $this->availableParts[$theme]['legs'][$body['legs']],
            'hair' => $this->availableParts[$theme]['hair'][$body['hair']],
            'arms' => $this->availableParts[$theme]['arms'][$body['arms']],
            'body' => $this->availableParts[$theme]['body'][$body['body']],
            'eyes' => $this->availableParts[$theme]['eyes'][$body['eyes']],
            'mouth' => $this->availableParts[$theme]['mouth'][$body['mouth']]
        );

        $avatar = imagecreate($widthHeight, $widthHeight);
        imagesavealpha($avatar, true);
        imagealphablending($avatar, false);

        $background = imagecolorallocate($avatar, 0,0,0);
        $line_colour = imagecolorallocate($avatar, mt_rand(0, 200) + 55, mt_rand(0, 200) + 55, mt_rand(0, 200) + 55);

        imagecolortransparent($avatar, $background);
        imagefilledrectangle($avatar, 0, 0, $widthHeight, $widthHeight, $background);

        // Fill avatar with random parts.
        foreach ($parts as &$part) {
            $this->drawPart($part, $avatar, $widthHeight, $line_colour, $background);
        }

        imagepng($avatar, $fileName);
        imagecolordeallocate($avatar, $line_colour);
        imagecolordeallocate($avatar, $background);
        imagedestroy($avatar);

        return $fileName;
    }

    private function drawPart($part, &$avatar, $widthHeight, $line_colour, $background)
    {
        $dotSize = floor($widthHeight / count($part));
        $offset = round(0.5 * ($widthHeight - $dotSize * count($part)));

        // Iterate over lines
        foreach ($part as $line_key => $line_val) {
            $y = $line_key * $dotSize + $offset;

            // Iterate over columns
            foreach ($line_val as $col_key => $col_val) {
                $x = $col_key * $dotSize + $offset;

                switch ($col_val) {
                    case 1:
                        imagefilledrectangle($avatar, $x, $y, $x + $dotSize - 1, $y + $dotSize - 1, $line_colour);
                        break;
                    case 2:
                        imagefilledrectangle($avatar, $x, $y, $x + $dotSize - 1, $y + $dotSize - 1, $background);
                        break;
                }
            }
        }
    }

    private function tmpFileExists($fileName)
    {
        if (!file_exists(_TMP_DIR_)) {
            mkdir(_TMP_DIR_, 0777, true);
        }

        return file_exists($fileName);
    }

